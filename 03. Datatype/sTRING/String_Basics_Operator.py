# STRING OPERATION

# ASSIGNING MULTIPLE VALUES TO MULTIPLE VARIABLE
str1 , str2 = "hi", "everyone"      # Assign multiple variable at once
str=str1+"! "+str2                  # Adding variables and string 
print(str)

# ADDING 2 STRING
add_string="hey! "+"everyone"          # store both string as one string
print("Add with + :", add_string)

# STORING MULTIPLE STRING TO A VARIABLE
join_string="hello","world"         # Store both string as list 
print("Add with , :",join_string)

# INDEXING (indexing in python starts with 0)
print(add_string[0])        # Print first char because it contain one string
print(join_string[0])       # Print first str because it contain multi str
print(join_string[1][0])    # Print first char of secondd string

